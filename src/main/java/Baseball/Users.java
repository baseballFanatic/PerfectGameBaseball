package Baseball;

public class Users
{
    private String username;
    private int season;

    public Users()
    {
    }

    public String getUsername()
    {
        return username;
    }

    public void setUsername( String username )
    {
        this.username = username;
    }

    public int getSeason()
    {
        return season;
    }

    public void setSeason( int season )
    {
        this.season = season;
    }
}
